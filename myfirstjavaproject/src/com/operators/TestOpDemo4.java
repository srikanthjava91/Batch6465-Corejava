package com.operators;

//4) Unary Operators 
//+ - ++ -- 
public class TestOpDemo4 {

	public static void main(String[] args) {
		System.out.println("main method started !");

		int a = 5;

		// unary -
		System.out.println(-a);

		int b = 6;
		// unary +
		System.out.println(+b);

		int c = -10;
		System.out.println(+c);

	}

}
